package task.task15.task1507;

public class Solution {
    public static void main(String[] args) {
        int temp = 36;
        switch (temp) {
            case 34:
                System.out.println("Очень низкая");
                break;
            case 35:
                System.out.println("Низкая");
                break;
            case 36:
                System.out.println("Низкая");
                break;
            case 37:
                System.out.println("Нормальная");
                break;
            case 38:
                System.out.println("Высокая");
                break;
            case 39:
                System.out.println("Очень высокая");
                break;
            case 40:
                System.out.println("Кипяток");
                break;
            default:
                System.out.println("Нужна скорая");
        }
    }
}
